async def handle_get_group_collected_stats(self, payload: Dict[str, Any]):
    """🔧 Phase8-P1: 獲取群組可收集用戶統計（優先讀緩存，fallback 到 COUNT 查詢）
    
    同時 return 統計結果，供 HTTP/Web 模式使用（前端依賴響應體中的 groupId/collectedUsers/monitoredMessages 更新「監控群組內容」）。
    """
    import sys
    
    group_id = payload.get('groupId')
    telegram_id = payload.get('telegramId')
    use_cache = payload.get('useCache', True)  # 默認使用緩存
    
    def _payload(gid, users, messages, from_cache=False):
        d = {"groupId": gid, "collectedUsers": users, "monitoredMessages": messages}
        if from_cache:
            d["fromCache"] = True
        return d
    
    try:
        from database import db
        await db.connect()
        
        collected_users = 0
        monitored_messages = 0
        chat_history_messages = 0
        
        # 🔧 Phase8-P1-2: 嘗試從緩存讀取
        if use_cache and group_id:
            try:
                cached = await db.fetch_one(
                    "SELECT cached_msg_count, cached_user_count FROM monitored_groups WHERE id = ? AND cached_msg_count > 0",
                    (group_id,)
                )
                if cached:
                    cm = cached['cached_msg_count'] if isinstance(cached, dict) else 0
                    cu = cached['cached_user_count'] if isinstance(cached, dict) else 0
                    if cm > 0:
                        out = _payload(group_id, cu, cm, from_cache=True)
                        self.send_event("group-collected-stats", out)
                        return {"success": True, **out}
            except Exception:
                pass  # 列可能不存在，繼續查詢
        
        if telegram_id:
            telegram_id_str = str(telegram_id)
            # 生成常見的 telegram_id 變體（解決 -100 前綴不一致問題）
            tid_variants = {telegram_id_str}
            bare_id = telegram_id_str.lstrip('-')
            if bare_id.startswith('100'):
                tid_variants.add(bare_id[3:])
                tid_variants.add(f"-{bare_id}")
            else:
                tid_variants.add(f"-100{bare_id}")
                tid_variants.add(f"100{bare_id}")
            
            # 數據源 1: discussion_messages（實時監控捕獲）
            for tid in tid_variants:
                try:
                    result = await db.fetch_one(
                        "SELECT COUNT(*) as msg_count, COUNT(DISTINCT user_id) as user_count FROM discussion_messages WHERE discussion_id = ? AND user_id IS NOT NULL AND user_id != ''",
                        (tid,)
                    )
                    if result:
                        mc = result['msg_count'] if isinstance(result, dict) else 0
                        uc = result['user_count'] if isinstance(result, dict) else 0
                        if mc > monitored_messages:
                            monitored_messages = mc
                        if uc > collected_users:
                            collected_users = uc
                except Exception:
                    pass
            
            # 數據源 2: chat_history（歷史消息 — 收集 handler 實際查詢的表）
            for tid in tid_variants:
                try:
                    result = await db.fetch_one(
                        "SELECT COUNT(*) as msg_count, COUNT(DISTINCT sender_id) as user_count FROM chat_history WHERE chat_id = ? AND sender_id IS NOT NULL AND sender_id != ''",
                        (tid,)
                    )
                    if result:
                        mc = result['msg_count'] if isinstance(result, dict) else 0
                        uc = result['user_count'] if isinstance(result, dict) else 0
                        chat_history_messages = max(chat_history_messages, mc)
                        if uc > collected_users:
                            collected_users = uc
                except Exception:
                    pass
            
            # 合併：取兩個數據源的最大值
            monitored_messages = max(monitored_messages, chat_history_messages)
            
            # 數據源 3: collected_users 表（已收集的用戶數）
            cu_count = 0
            for tid in tid_variants:
                try:
                    cu_result = await db.fetch_one(
                        "SELECT COUNT(*) as count FROM collected_users WHERE source_group = ?",
                        (tid,)
                    )
                    if cu_result:
                        c = cu_result['count'] if isinstance(cu_result, dict) else 0
                        cu_count = max(cu_count, c)
                except Exception:
                    pass
            
            final_users = cu_count if cu_count > 0 else collected_users
        else:
            final_users = collected_users
        
        # 🔧 Phase8-P1-2: 寫入緩存
        if group_id and monitored_messages > 0:
            try:
                await db.execute(
                    "UPDATE monitored_groups SET cached_msg_count = ?, cached_user_count = ? WHERE id = ?",
                    (monitored_messages, final_users, group_id)
                )
            except Exception:
                pass
        
        out = _payload(group_id, final_users, monitored_messages)
        self.send_event("group-collected-stats", out)
        return {"success": True, **out}
        
    except Exception as e:
        import traceback
        traceback.print_exc(file=sys.stderr)
        out = _payload(group_id, 0, 0)
        out["error"] = str(e)
        self.send_event("group-collected-stats", out)
        return {"success": False, **out}

